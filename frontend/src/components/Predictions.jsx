import { LineChart, Line, XAxis, YAxis, CartesianGrid, Tooltip as RechartsTooltip, ResponsiveContainer, Legend } from 'recharts';
import { Target, TrendingUp, Zap } from 'lucide-react';

const Predictions = ({ results }) => {
  if (!results) return null;

  const { forecasts } = results;
  const productKeys = Object.keys(forecasts);
  
  if (productKeys.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center p-12 premium-card">
        <Target size={48} className="text-slate-500 mb-4" />
        <h3 className="text-xl font-bold text-slate-800 mb-2">Insufficient Data for Forecasts</h3>
        <p className="text-slate-500">The dataset needs date and quantity columns to generate models.</p>
      </div>
    );
  }

  // Format data for chart (assuming all products have same length 'daily' array for next 7 days)
  const days = forecasts[productKeys[0]].daily.length;
  const chartData = Array.from({ length: days }).map((_, dayIndex) => {
    const dataPoint = { day: `Day ${dayIndex + 1}` };
    productKeys.forEach(prod => {
      dataPoint[prod] = Math.round(forecasts[prod].daily[dayIndex]);
    });
    return dataPoint;
  });

  const colors = ['#3b82f6', '#10b981', '#f59e0b', '#ef4444', '#8b5cf6', '#ec4899'];

  return (
    <div className="space-y-6">
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-2xl font-bold text-slate-800 mb-1">Demand Forecasting</h2>
          <p className="text-slate-500">7-Day outlook generated by Linear Regression AI</p>
        </div>
        <div className="flex bg-slate-100 p-1 rounded-xl">
          <button className="px-4 py-2 text-sm font-medium rounded-lg bg-white text-slate-800 shadow">7 Days</button>
          <button className="px-4 py-2 text-sm font-medium rounded-lg text-slate-500 hover:text-slate-800">30 Days (Demo)</button>
        </div>
      </div>

      <div className="premium-card p-6 h-96">
        <h3 className="text-lg font-bold text-slate-800 mb-6 flex items-center gap-2">
          <TrendingUp className="text-blue-400" />
          Projected Unit Sales
        </h3>
        <ResponsiveContainer width="100%" height="100%">
          <LineChart data={chartData}>
            <CartesianGrid strokeDasharray="3 3" stroke="#334155" vertical={false} />
            <XAxis dataKey="day" stroke="#94a3b8" tick={{fill: '#94a3b8'}} tickLine={false} axisLine={false} />
            <YAxis stroke="#94a3b8" tick={{fill: '#94a3b8'}} tickLine={false} axisLine={false} />
            <RechartsTooltip 
              contentStyle={{ backgroundColor: '#ffffff', border: '1px solid #e2e8f0', borderRadius: '8px' }}
              itemStyle={{ color: '#1e293b' }}
            />
            <Legend wrapperStyle={{ paddingTop: '20px' }} />
            {productKeys.slice(0, 5).map((prod, idx) => (
              <Line 
                key={prod} 
                type="monotone" 
                dataKey={prod} 
                stroke={colors[idx % colors.length]} 
                strokeWidth={3}
                dot={{ stroke: colors[idx % colors.length], strokeWidth: 2, r: 4, fill: '#1e293b' }}
                activeDot={{ r: 6, fill: colors[idx % colors.length] }}
              />
            ))}
          </LineChart>
        </ResponsiveContainer>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
        {productKeys.slice(0, 3).map((prod, idx) => (
          <div key={prod} className="premium-card p-6 border-t-4" style={{borderTopColor: colors[idx % colors.length]}}>
            <h4 className="text-slate-500 text-sm mb-1 font-bold">{prod}</h4>
            <p className="text-3xl font-black text-slate-800 mb-4">
              {Math.round(forecasts[prod].total_predicted_demand)} <span className="text-sm font-normal text-slate-500">units/wk</span>
            </p>
            <div className="flex items-center gap-2 text-xs font-semibold text-emerald-400 bg-emerald-400/10 w-fit px-3 py-1 rounded-full">
              <Zap size={14} />
              High Confidence
            </div>
          </div>
        ))}
      </div>
    </div>
  );
};

export default Predictions;
